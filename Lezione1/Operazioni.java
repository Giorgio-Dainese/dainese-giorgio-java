// Scrivere un programma con variabili a e b intere e c e d double, utilizzare le 4+1 operazioni e fare print

public class Operazioni{
    public static void main(String args[]){
        int a = 21;
        int b = 4;
        double c = 5.5;
        double d = 1.1;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);

        System.out.println(a+d);
        System.out.println(a-d);
        System.out.println(a*d);
        System.out.println(a/d);
        System.out.println(a%d);
    }
}